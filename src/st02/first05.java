package st02;

public class first05 {
    public static void main(String[] args) {

        int number = 10;

        number = 10;

        number += 2;  //12
        System.out.println(number);

        number -= 2;   //10
        System.out.println(number);

        number *= 2;  //20
        System.out.println(number);

        number /= 2;   //10
        System.out.println(number);

        number %= 2;   //0
        System.out.println(number);

        number++;
        System.out.println(number);

        number--;
        System.out.println(number);
    }
}
